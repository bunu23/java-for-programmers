public class Demo {
    public static void main(String[] args) {

        // Byte
        byte minByte = Byte.MIN_VALUE;
        byte maxByte = Byte.MAX_VALUE;
        System.out.println("Byte range: from " + minByte + " to " + maxByte);

        // Short
        short minShort = Short.MIN_VALUE;
        short maxShort = Short.MAX_VALUE;
        System.out.println("Short range: from " + minShort + " to " + maxShort);

        // Int
        int minInt = Integer.MIN_VALUE;
        int maxInt = Integer.MAX_VALUE;
        System.out.println("Integer range: from " + minInt + " to " + maxInt);

        // Long
        long minLong = Long.MIN_VALUE;
        long maxLong = Long.MAX_VALUE;
        System.out.println("Long range: from " + minLong + " to " + maxLong);

        // Float
        float minFloat = Float.MIN_VALUE;
        float maxFloat = Float.MAX_VALUE;
        System.out.println("Float range: from " + minFloat + " to " + maxFloat);

        // Double
        double minDouble = Double.MIN_VALUE;
        double maxDouble = Double.MAX_VALUE;
        System.out.println("Double range: from " + minDouble + " to " + maxDouble);

        // Char
        char minChar = Character.MIN_VALUE;
        char maxChar = Character.MAX_VALUE;
        System.out.println("Char range: from " + (int)minChar + " to " + (int)maxChar);
// Boolean
        boolean trueValue = true;
        boolean falseValue = false;
        System.out.println("Boolean values: " + falseValue + " and " + trueValue);



    }

    }

